// STAMPARE NUMERI CON UN CICLO

// Programma che stampa i numeri da 1 a 10 in una riga

#include <stdio.h>

// la funzione main inizia l'esecuzione del programma
int main(void) {

   int n = 1;
   
   while (n <= 10) {
   
      printf("%d   ", n++);
   
   } // fine di while

} // fine della funzione main
